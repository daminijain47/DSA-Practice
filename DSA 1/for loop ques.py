for i in range(10): #bydefault 0 se start honga
    print(i)
for i in range(1,10): #1 se print honga
    print(i)

for i in range(1,11, 1): #ek ek gap mai karenga 
    print(i)


for i in range(1,11, 2): #2 ke gap mai aayega 
    print(i)

for i in range(10,0 ): #koi vaule nahi aayegei
    print(i)

for i in range(10,0,-2): #reverse no print honge
    print(i)
